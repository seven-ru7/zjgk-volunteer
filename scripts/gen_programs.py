def get_categories(inst_name: str) -> list[str]:
    """返回院校强势类别。精确匹配 → 关键字匹配 → 默认综合。"""
    if inst_name in INSTITUTION_CATEGORIES:
        return INSTITUTION_CATEGORIES[inst_name]
    # 关键字兜底
    for key, cats in INSTITUTION_CATEGORIES.items():
        if key in inst_name or inst_name in key:
            return cats
    return ["文史", "理工"]


def main():
    institutions = json.loads((DATA_DIR / "institutions.json").read_text(encoding="utf-8"))
    programs = []
    pid = 0
    for inst in institutions:
        name = inst["name"]
        city = inst["city"]
        cats = set(get_categories(name))
        # 选 5-7 个匹配专业
        candidates = [t for t in PROGRAM_TEMPLATES if any(c in cats for c in t[4])]
        # 每校固定取 5-6 个（按模板顺序）
        n = min(6, len(candidates))
        selected = candidates[:n]
        for tpl_name, dur, fee, subj, _ in selected:
            pid += 1
            programs.append({
                "program_id": f"P{pid:04d}",
                "name": tpl_name,
                "institution": name,
                "city": city,
                "duration_years": dur,
                "tuition": fee,
                "required_subjects": subj,
                "plan_quota_2026": 0,  # 爬取后填充
                "history": [],  # 爬取后填充
            })
    out = DATA_DIR / "programs.json"
    out.write_text(json.dumps(programs, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"✓ 已生成 {len(programs)} 个专业 → {out}")
    # 统计院校覆盖率
    inst_names = {p["institution"] for p in programs}
    print(f"  - 覆盖院校：{len(inst_names)}/{len(institutions)}")
    no_prog = set(i["name"] for i in institutions) - inst_names
    if no_prog:
        print(f"  ⚠ 未匹配专业的院校：{no_prog}")


if __name__ == "__main__":
    main()
